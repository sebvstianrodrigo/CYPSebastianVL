X1 = float(input("Introduce la coordenada X del punto P1:"))
Y1 = float(input("Introduce la coordenada Y del punto P1:"))
X2 = float(input("Introduce la coordenada X del punto P2:"))
Y2 = float(input("Introduce la coordenada Y del punto P2:"))
DIS = ((X1 - X2) ** 2 + (Y1 - Y2) ** 2) ** 0.5
print(f"La distancia entre P1 y P2 es {DIS}")
