# El algoritmo, dadas cinco calificaciones de un alumno, calcula su promedio
MAT = int(input("Introduce tu matricula:"))
CAL1 = int(input("Introduce la primera calificacion:"))
CAL2 = int(input("Introduce la segunda calificacion:"))
CAL3 = int(input("Introduce la tercera calificacion:"))
CAL4 = int(input("Introduce la cuarta calificacion:"))
CAL5 = int(input("Introduce la quinta calificacion:"))
PRO = (CAL1+CAL2+CAL3+CAL4+CAL5)/5
print(MAT)
print(PRO)
